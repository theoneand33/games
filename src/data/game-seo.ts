export interface GameSEO {
  title: string;
  description: string;
  image: string;
  genre: string;
  year?: string;
  isFlash: boolean;
  gamePath?: string;
}

function seo(
  title: string,
  genre: string,
  year: string,
  image: string,
  isFlash: boolean,
  hook: string,
  gamePath = "",
): GameSEO {
  return {
    title,
    description: `Play ${title} unblocked online. ${hook}`,
    image,
    genre,
    year,
    isFlash,
    gamePath,
  };
}

