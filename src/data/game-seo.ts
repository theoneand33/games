export interface GameSEO {
  title: string;
  description: string;
  image: string;
  genre: string;
  year?: string;
  keywords: string[];
  isFlash: boolean;
}

export function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}

function seo(
  title: string,
  genre: string,
  year: string,
  image: string,
  keywords: string[],
  isFlash: boolean,
  hook: string,
): GameSEO {
  return {
    title,
    description: `Play ${title} unblocked online. ${hook}`,
    image,
    genre,
    year,
    keywords,
    isFlash,
  };
}

